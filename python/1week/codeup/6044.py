
a, b = input().split()

# 합
plus = int(a) + int(b)

# 차
minus = int(a) - int(b)

# 곱
multiply = int(a) * int(b)

# 몫
quotient = int(a) // int(b)

# 나머지
remain = int(a) % int(b)

# 나눈 값
quotient_value = int(a) / int(b)

print(plus)
print(minus)
print(multiply)
print(quotient)
print(remain)
print(format(quotient_value, ".2f"))